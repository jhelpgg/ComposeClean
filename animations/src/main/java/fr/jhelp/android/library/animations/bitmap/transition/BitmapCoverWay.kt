package fr.jhelp.android.library.animations.bitmap.transition

/**
 * Way the cover progress in [BitmapCoverTransition]
 */
enum class BitmapCoverWay
{
    /** Go left side to right side */
    LEFT_TO_RIGHT,

    /** Go right side to left side */
    RIGHT_TO_LEFT,

    /** Go top side to bottom side */
    TOP_TO_BOTTOM,

    /** Go bottom side to top side */
    BOTTOM_TO_TOP
}